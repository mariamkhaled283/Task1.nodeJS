//////////////////////////////////////////// (1) create object person  ///////////////////////////////////////////
const person = {
   fname : "Ahmed",
   lname : "Hossam",
   age : "20",
   city : "Alex"

}
 module.exports = {
   Person1 : person
 }

////////////////////////////////////////////////   (2) change obj to Json   //////////////////////////////////////////////////

const personJson = JSON.stringify(person)
console.log(personJson)

/////////////////////////////////////////////////   (3)  store in file    /////////////////////////////////////////////////

const fs = require("fs")

fs.writeFileSync("detailData.txt" , personJson)

///////////////////////////////////////////////    (4) read file (json)   ///////////////////////////////////////////////

console.log(fs.readFileSync("detailData.txt").toString())

//////////////////////////////////////////////    (5)  Convert to obj    ///////////////////////////////////////////////

const personObj = JSON.parse(personJson)
console.log(personObj)

////////////////////////////////////////////     (6) Update data to ( adel , ahmed , 40 , cairo)    ///////////////////////////////////////////

personObj.fname = "Adel"
personObj.lname = "Ahmed"
personObj.age = "40"
personObj.city = "Cairo"

console.log(personObj)

////////////////////////////////////////    (7) convert obj to Json      ////////////////////////////////////////////
const personObjJson = JSON.stringify(personObj)
console.log(personObjJson)

///////////////////////////////////////    (8) store in file (writeFileSync)     //////////////////////////////////////////////

fs.writeFileSync("detailData.txt" , personObjJson)



